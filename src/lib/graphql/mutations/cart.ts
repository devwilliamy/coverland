export const UPDATE_CART = gql`
  mutation UpdateCartLine($cartId: ID!, $lines: [CartLineUpdateInput!]!) {
    cartLinesUpdate(cartId: $cartId, lines: $lines) {
      cart {
        id
        lines(first: 10) {
          edges {
            node {
              id
              quantity
              merchandise {
                ... on ProductVariant {
                  id
                  image {
                    url
                  }
                  title
                  price {
                    amount
                    currencyCode
                  }
                  product {
                    id
                    productType
                    title
                  }
                  sku
                }
              }
            }
          }
        }
      }
    }
  }
`;
