import { gql } from '@apollo/client';

export const FETCH_CART = gql`
  query fetchCart($cartId: ID!) {
    cart(id: $cartId) {
      id
      lines(first: 10) {
        edges {
          node {
            id
            quantity
            merchandise {
              ... on ProductVariant {
                id
                title
                price {
                  amount
                  currencyCode
                }
              }
            }
          }
        }
      }
      totalQuantity
      totalPrice {
        amount
        currencyCode
      }
    }
  }
`;

